"""Build, verify, and install the public full-market dataset release."""

from __future__ import annotations

import hashlib
import json
import os
import shutil
from collections.abc import Callable
from pathlib import Path
from typing import Any

import pandas as pd

FULL_DATASET_REPO = "ANTICH/traderharness-ashare-5y"


class DatasetIntegrityError(RuntimeError):
    """Raised when a downloaded or staged dataset fails verification."""


def build_public_news(source: pd.DataFrame) -> pd.DataFrame:
    """Return a schema-compatible release without redistributing CLS正文."""
    public = source.copy()
    titles = public.get("title", pd.Series("", index=public.index)).fillna("").astype(str)
    has_title = titles.str.strip().ne("")
    public["content"] = titles.where(has_title, "快讯正文未随公开数据集分发。")
    public["content_source"] = has_title.map({True: "derived_headline", False: "redacted"})
    return public


